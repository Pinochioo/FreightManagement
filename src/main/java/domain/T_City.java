package main.java.domain;

public class T_City {
	private int id;	//省市编号
	private String sf;	//省份名称
	private String cs;	//城市名称
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSf() {
		return sf;
	}
	public void setSf(String sf) {
		this.sf = sf;
	}
	public String getCs() {
		return cs;
	}
	public void setCs(String cs) {
		this.cs = cs;
	}

	
}
